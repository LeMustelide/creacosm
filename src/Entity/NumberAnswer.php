<?php

namespace App\Entity;

use App\Repository\NumberAnswerRepository;
use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\Common\Collections\Collection;
use Doctrine\ORM\Mapping as ORM;

#[ORM\Entity(repositoryClass: NumberAnswerRepository::class)]
class NumberAnswer
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    #[ORM\Column]
    private ?float $value = null;

    #[ORM\ManyToOne(inversedBy: 'numberAnswers')]
    #[ORM\JoinColumn(nullable: false)]
    private ?QuestionNumber $question = null;

    #[ORM\OneToMany(mappedBy: 'numberAnswer', targetEntity: History::class)]
    private Collection $histories;

    public function __construct()
    {
        $this->histories = new ArrayCollection();
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getValue(): ?float
    {
        return $this->value;
    }

    public function setValue(float $value): self
    {
        $this->value = $value;

        return $this;
    }

    public function getQuestion(): ?QuestionNumber
    {
        return $this->question;
    }

    public function setQuestion(?QuestionNumber $question): self
    {
        $this->question = $question;

        return $this;
    }

    /**
     * @return Collection<int, History>
     */
    public function getHistories(): Collection
    {
        return $this->histories;
    }

    public function addHistory(History $history): self
    {
        if (!$this->histories->contains($history)) {
            $this->histories->add($history);
            $history->setNumberAnswer($this);
        }

        return $this;
    }

    public function removeHistory(History $history): self
    {
        if ($this->histories->removeElement($history)) {
            // set the owning side to null (unless already changed)
            if ($history->getNumberAnswer() === $this) {
                $history->setNumberAnswer(null);
            }
        }

        return $this;
    }
}
